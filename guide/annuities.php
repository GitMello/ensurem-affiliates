        <h3>Who purchases variable annuities?</h3>
        <hr>
        <p>Variable annuities tend to appeal to pre-retirees and retirees who are interested in the possibility of capital appreciation in tandem with guaranteed lifetime income.</p>
        <p>Variable annuities allow investors to choose from a basket of subaccounts (mutual funds). Unlike fixed indexed annuities, the principal can go down in value based on market performance.</p>
        <img src="/assets/discover/discover-annuities-3" alt="Injured on the job. Income protected.">
      </div>
      <div class="col-md-6 col-lg-4 col-pad flex-grow-1 mid">
        <h2>Fixed Indexed Annuities</h2>
        <h3>Who purchases fixed indexed annuities?</h3>
        <hr>
        <p>Fixed indexed annuities tend to appeal to pre-retirees and retirees who want to conservatively participate in potential market appreciation while enjoying downside principal protection.</p>
        <p>Fixed indexed annuities are essentially fixed annuities with a variable rate of interest that is added to the contract value if an underlying market index, such as the S&amp;P 500, is positive. They typically offer a guaranteed minimum income benefit, and the chance of principal upside linked to a market-based index. A negative is that upside potential is limited by a so-called participation rate, caps or a spread — all methods in which the return in a rising stock market is trimmed.</p>
      </div>
      <div class="col-md-6 col-lg-4 col-pad flex-grow-1 mid-dark">
        <h2>Immediate Annuities</h2>
        <h3>Who purchases immediate annuities?</h3>
        <hr>
        <p>Immediate annuities tend to appeal to pre-retirees and retirees who need a higher-than-average stream of income and are comfortable sacrificing principal in exchange for higher lifelong income.</p>
        <p>Immediate annuities closely mirror a life insurance policy. Instead of paying regular premiums to an insurer that makes a lump-sum payment upon death, the owner/annuitant gives the insurer a lump sum in return for regular income payments until death, or for a specified period of time, typically starting one to 12 months after receipt of the investment. Payments are typically higher than other annuities because they include principal, as well as interest, and so also offer favorable tax treatment.</p>
      </div>
      <div class="col-md-6 col-lg-4 col-pad flex-grow-1 dark">
          <h2>Deferred Annuities</h2>
          <h3>Who purchases deferred annuities?</h3>
          <hr>
          <p>Deferred annuities tend to appeal to people who want guaranteed income in the future, not now, or who want to create a ladder of income over different periods later in life. For example, they may want to work in retirement but know that eventually they will stop working and, at that point, and not before, will need guaranteed income from an annuity.</p>
        <p>Deferred annuities delay payments until a future date (greater than one year). They enable the owner and/or annuitant to increase their income stream later in life for less money.</p>
      </div>
