      <h2>Coinsurance</h2>
      <p>The percentage of costs of a covered health care service you pay after you’ve paid your deductible. Unlike Obamacare plans, you will also be responsible for paying coinsurance up to your out-of-pocket maximum with your plan. You will still have to meet your deductible before the insurance company will pay anything. For example, let’s say you need $3,000 of medical services and your deductible is $2,000. The insurance company will pay a percentage of the $1,000 in expenses after your deductible was met. If they pay 80% your coinsurance payment would be $200. Even with coinsurance, your health care expenses are still typically much lower than the Obamacare deductible.</p>
