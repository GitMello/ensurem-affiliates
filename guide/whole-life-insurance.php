      <h2>How Whole Life Insurance works</h2>
      <p>Your monthly premium for a whole life insurance policy stays the same through the life of the contract, making it a more affordable option for those who want life insurance coverage for years to come. And, unlike term insurance, a whole life policy is a permanent policy — it will provide coverage through your entire lifetime. If you ever stopped making premium payments, you could surrender the policy and receive full cash value of your policy. You will need to review the terms of your contract closely to determine the available options.</p>
